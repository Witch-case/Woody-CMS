
function showArchivedAttributes()
{
    document.getElementById('archivedAttributes').style.display = 'block';
    document.getElementById('showArchivesHref').style.display = 'none';
}

function hideArchivedAttributes()
{
    document.getElementById('archivedAttributes').style.display = 'none';
    document.getElementById('showArchivesHref').style.display = 'block';
}
